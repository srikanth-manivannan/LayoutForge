_FONT_FORMAT_BY_EXT = {
    "ttf": "truetype",
    "otf": "opentype",
    "woff": "woff",
    "woff2": "woff2",
}


def _font_face_rule(font: FontResource) -> str | None:
    if not font.filename:
        return None
    ext = font.filename.rsplit(".", 1)[-1].lower()
    fmt = _FONT_FORMAT_BY_EXT.get(ext, "truetype")
    style = "italic" if font.style == "italic" else "normal"
    # Relative from resources/css/ (where this file lives) to resources/fonts/.
    return (
        f"@font-face {{\n"
        f'  font-family: "{css_family_name(font)}";\n'
        f'  src: url("../fonts/{font.filename}") format("{fmt}");\n'
        f"  font-weight: {css_weight(font)};\n"
        f"  font-style: {style};\n"
        f"}}"
    )


def _build_common_css(document: Document) -> str:
    lines = [
        "/* Generated by LayoutForge — shared rules reused across every page. */",
        # Reset the UA body margin so .lf-page sits at (0,0) — the page's
        # absolute coordinates are page-space, and any body offset (default
        # 8px) would shift the whole overlay off the background raster.
        "html, body { margin: 0; padding: 0; }",
        ".lf-page { position: relative; overflow: hidden; background-color: #ffffff; }",
        ".lf-page-background { position: absolute; top: 0; left: 0; width: 100%; height: 100%; z-index: 0; }",
        # RIL Rule 0 DOM: exactly ONE absolute anchor per region; paragraphs
        # inside are NORMAL-FLOW blocks (not position:absolute) so the
        # browser stacks them — a paragraph that renders taller than its PDF
        # measurement pushes the next one down instead of overlapping it.
        # Margin-top (PDF-measured inter-paragraph gap) provides spacing;
        # height is never asserted. THE BROWSER WRAPS THE LINES (lines are
        # engine concepts and never reach HTML). `pre-wrap` preserves the
        # PDF's exact spaces (incl. hair/thin spaces) while allowing
        # wrapping; kerning/ligatures disabled so the browser's advance
        # model matches the measurement engine's.
        ".lf-region { position: absolute; }",
        ".lf-paragraph { margin: 0; white-space: pre-wrap; "
        "font-kerning: none; font-variant-ligatures: none; }",
        ".lf-image { position: absolute; transform-origin: top left; }",
        "",
    ]
    # Rendering Stabilization phase (temporary): the Style Registry is
    # bypassed — every paragraph/run carries a COMPLETE inline style (see
    # html_compiler.py), so no dedup classes/rules are emitted here.
    # One @font-face per FontResource — css_family_name gives every
    # extracted font file its own unique family, so two subsets of the same
    # typeface can never shadow each other (each text run loads exactly the
    # file it was extracted with; see font_naming.py).
    for font in document.fonts:
        rule = _font_face_rule(font)
        if rule:
            lines.append(rule)
            lines.append("")
    return "\n".join(lines)


def _build_page_css(page: Page, fonts_by_id: dict[str, FontResource]) -> str:
    # RIL: text geometry/style now come from the Render Tree (inline geometry
    # + Style Registry classes in common.css) — per-block #tb rules retired
    # with the legacy renderer. Page CSS keeps page size and image rules.
    lines = [
        f"/* Generated by LayoutForge — page {page.number}. */",
        f".lf-page-{page.number} {{ width: {page.width:g}px; height: {page.height:g}px; }}",
        "",
    ]

    for image in page.images:
        declarations = [
            f"left: {image.bbox.x:g}px",
            f"top: {image.bbox.y:g}px",
            f"width: {image.bbox.width:g}px",
            f"height: {image.bbox.height:g}px",
            f"z-index: {image.z_index}",
        ]
        if image.rotation:
            declarations.append(f"transform: rotate({image.rotation:g}deg)")
        rule_body = ";\n  ".join(declarations)
        lines.append(f"#img-{image.id} {{\n  {rule_body};\n}}")
        lines.append("")

    return "\n".join(lines)


class CssOutputPlugin(OutputPlugin):
    """Reads only the IDM and writes resources/css/common.css plus one
    resources/css/page_XXXX.css per page. Uses relative URLs (../fonts/...)
    so the project workspace is self-contained — the same files render
    identically whether opened from disk, served by the backend, or loaded
    into an iframe, with no per-renderer URL rewriting."""

    def __init__(self, storage_service: StorageService) -> None:
        self._storage = storage_service

    @property
    def name(self) -> str:
        return "css"

    def generate(self, context: PipelineContext) -> list[tuple[int, str]]:
        document = context.document
        assert document is not None, "Extraction stages must run before CssOutputPlugin"

        css_dir = self._storage.project_dir(context.project_id) / "resources" / "css"
        css_dir.mkdir(parents=True, exist_ok=True)

        # RIL: build + VALIDATE the Render Trees once here (deterministic,
        # derived — kept only in scratch for HtmlOutputPlugin, then discarded
        # with the context).
        trees: dict[int, object] = {}
        for page in document.pages:
            if page.text_blocks and not page.regions:
                raise RuntimeError(
                    f"page {page.number} has text but no reconstructed regions — "
                    "ReconstructTreeStage must run before output generation"
                )
            tree = build_render_tree(document, page)
            validate_render_tree(tree, page)  # RIL Rule 6: gate before any compiler
            trees[page.number] = tree
        context.scratch["ril_trees"] = trees

        (css_dir / "common.css").write_text(_build_common_css(document), encoding="utf-8")

        fonts_by_id = {font.id: font for font in document.fonts}
        generated: list[tuple[int, str]] = []
        for page in document.pages:
            filename = f"page_{page.number:04d}.css"
            (css_dir / filename).write_text(_build_page_css(page, fonts_by_id), encoding="utf-8")
            generated.append((page.number, f"resources/css/{filename}"))

        return generated
